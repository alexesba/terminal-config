# Terminal values gogh accepts (the colorscheme function passes TERMINAL straight
# to gogh's apply-colors.sh). Mirror of that script's dispatch — keep in sync if
# gogh adds terminals. install.sh only ships config templates for the first three.
COLORSCHEME_TERMINALS="alacritty kitty wezterm konsole gnome-terminal mate-terminal \
xfce4-terminal tilix guake foot terminator mintty iTerm.app pantheon-terminal \
io.elementary.terminal kmscon linux termux"

# Returns 0 when $1 is a terminal gogh/colorscheme can theme. Matches gogh's
# prefix cases for gnome-terminal* and io.elementary.t* as well.
# Usage: is_colorscheme_terminal <name>
is_colorscheme_terminal() {
  local term="${1:-}"
  [[ -z "$term" ]] && return 1
  case "$term" in
    gnome-terminal*|io.elementary.t*) return 0 ;;
  esac
  case " ${COLORSCHEME_TERMINALS} " in
    *" ${term} "*) return 0 ;;
    *) return 1 ;;
  esac
}

# Returns 0 when an install.sh INSTALL_* option only works if ~/{.zshrc,.bashrc}
# sources rc.sh (aliases, shell functions, plugin init, etc.).
# Usage: needs_shell_rc_wrapper <OPTION_SUFFIX>
needs_shell_rc_wrapper() {
  case "$1" in
    ALIASES|AUTOSUGG|RBENV|NVM|FZF|GOGH) return 0 ;;
    *) return 1 ;;
  esac
}

# Returns 0 when an install.sh INSTALL_* option only wires into FZF via rc.sh
# (file listing, previews, or the colorscheme picker).
# Usage: needs_fzf_for_install <OPTION_SUFFIX>
needs_fzf_for_install() {
  case "$1" in
    RIPGREP|BAT|GOGH) return 0 ;;
    *) return 1 ;;
  esac
}

# Machine-local env, secrets, and install-managed exports (TERMINAL, fonts, …).
# Usage: local_sh_path
local_sh_path() {
  printf '%s/.local.sh\n' "$HOME"
}

# Migrate legacy paths to ~/.local.sh (repo shell/custom.sh, then ~/.custom.sh).
# Usage: migrate_local_sh <dotfiles_dir>
migrate_local_sh() {
  local dotfiles_dir="$1"
  local dest legacy_repo legacy_home
  dest="$(local_sh_path)"

  [ -f "$dest" ] && return 0

  legacy_repo="$dotfiles_dir/shell/custom.sh"
  if [ -f "$legacy_repo" ]; then
    cp "$legacy_repo" "$dest"
    rm -f "$legacy_repo"
    echo -e "  ${GREEN}✓${RESET}  Migrated ${DIM}shell/custom.sh${RESET} → ${DIM}~/.local.sh${RESET}"
    return 0
  fi

  legacy_home="${HOME}/.custom.sh"
  if [ -f "$legacy_home" ]; then
    cp "$legacy_home" "$dest"
    rm -f "$legacy_home"
    echo -e "  ${GREEN}✓${RESET}  Migrated ${DIM}~/.custom.sh${RESET} → ${DIM}~/.local.sh${RESET}"
  fi
}

# Installs a local ~/.zshrc or ~/.bashrc that sources rc.sh from this repo.
# Other tools can append their own lines below the managed block.
#
# Usage: install_shell_rc_wrapper <rc.sh> <dest>
install_shell_rc_wrapper() {
  local src="$1"
  local dest="$2"
  local tmp preserved header footer block inblock

  src="$(cd "$(dirname "$src")" && pwd)/$(basename "$src")"

  header="# $(basename "$dest") — local shell startup (managed by terminal-config)
# Generated by install.sh / update.sh — safe for other tools to append below."
  footer="# Other tools (nvm, conda, etc.) may append below this line."
  block="# terminal-config: begin
source \"$src\"
# terminal-config: end"

  if [ -f "$dest" ] && grep -qF '# terminal-config: begin' "$dest"; then
    tmp=$(mktemp)
    inblock=0
    while IFS= read -r line || [ -n "$line" ]; do
      if [ "$line" = "# terminal-config: begin" ]; then
        printf '%s\n' "# terminal-config: begin"
        printf 'source "%s"\n' "$src"
        printf '%s\n' "# terminal-config: end"
        inblock=1
        continue
      fi
      if [ "$inblock" = 1 ] && [ "$line" = "# terminal-config: end" ]; then
        inblock=0
        continue
      fi
      [ "$inblock" = 1 ] && continue
      printf '%s\n' "$line"
    done <"$dest" >"$tmp"
    mv "$tmp" "$dest"
    echo -e "  ${GREEN}✓${RESET}  $dest updated (terminal-config source refreshed)."
    return 0
  fi

  if [ -L "$dest" ]; then
    local link_target
    link_target="$(readlink "$dest")"
    [[ "$link_target" != /* ]] && link_target="$(cd "$(dirname "$dest")" && pwd)/$link_target"
    if [ "$link_target" = "$src" ]; then
      echo -e "  ${DIM}Converting $dest symlink → local wrapper.${RESET}"
    else
      echo -e "  ${YELLOW}⚠${RESET}  $dest points elsewhere — backing up as ${DIM}$dest.old${RESET}"
      mv "$dest" "$dest.old"
    fi
    [ -L "$dest" ] && rm "$dest"
  elif [ -f "$dest" ]; then
    echo -e "  ${YELLOW}⚠${RESET}  $dest already exists — backing up as ${DIM}$dest.old${RESET}"
    mv "$dest" "$dest.old"
    preserved=$(cat "${dest}.old")
  fi

  {
    printf '%s\n\n' "$header"
    printf '%s\n\n' "$block"
    printf '%s\n' "$footer"
    if [ -n "${preserved:-}" ]; then
      printf '\n%s\n' "$preserved"
    fi
  } >"$dest"
  echo -e "  ${GREEN}✓${RESET}  $dest installed (sources rc.sh; other tools may append below)."
}

