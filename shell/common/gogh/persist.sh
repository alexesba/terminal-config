#!/usr/bin/env bash
# Persist a gogh theme so it survives new terminal windows / restarts.
#
# Usage: persist.sh /path/to/theme.sh <terminal>
#        persist.sh --terminal <name>   # update terminal= only (sync / tmux-start)
set -u

# Update terminal= in state without re-running a theme (sync_terminal_to_host / tmux-start).
record_gogh_terminal() {
  local term="${1:-}"
  [ -n "$term" ] || return 0
  local state_dir="${XDG_STATE_HOME:-$HOME/.local/state}/gogh"
  local current="${state_dir}/current"
  mkdir -p "$state_dir"
  if [ ! -f "$current" ]; then
    printf 'terminal=%s\n' "$term" >"$current"
    return 0
  fi
  if grep -q '^terminal=' "$current"; then
    grep -v '^terminal=' "$current" >"${current}.tmp"
    printf 'terminal=%s\n' "$term" >>"${current}.tmp"
    mv "${current}.tmp" "$current"
  else
    printf 'terminal=%s\n' "$term" >>"$current"
  fi
}

case "${1:-}" in
  --terminal)
    record_gogh_terminal "${2:-}"
    exit 0
    ;;
esac

file="${1:-}"
term="${2:-}"
[ -f "$file" ] || exit 0

# Parse "#RRGGBB" from theme export without sourcing the script.
field() {
  sed -n "s/^export $1=\"\(#[0-9A-Fa-f]\{6\}\)\".*/\1/p" "$file" | head -n1
}

# Write name=, file=, terminal= to ~/.local/state/gogh/current.
record_current_theme() {
  local theme="$1"
  local term="${2:-}"
  local state_dir="${XDG_STATE_HOME:-$HOME/.local/state}/gogh"
  local name
  mkdir -p "$state_dir"
  name=$(sed -n 's/^export PROFILE_NAME="\([^"]*\)".*/\1/p' "$theme" | head -n1)
  [ -z "$name" ] && name=$(basename "$theme" .sh)
  {
    echo "name=$name"
    echo "file=$(basename "$theme")"
    [ -n "$term" ] && echo "terminal=$term"
  } >"${state_dir}/current"
}

record_current_theme "$file" "$term"

# WezTerm: Gogh only OSC-the live pane; write colors.lua for new panes/windows.
case "$term" in
  wezterm)
    cfg="${WEZTERM_CONFIG_DIR:-$HOME/.config/wezterm}"
    mkdir -p "$cfg"
    out="$cfg/colors.lua"
    cursor=$(field CURSOR_COLOR)
    {
      echo "-- Generated by the colorscheme() shell function — do not edit by hand."
      echo "-- Source theme: $(basename "$file")"
      echo "return {"
      echo "  foreground = \"$(field FOREGROUND_COLOR)\","
      echo "  background = \"$(field BACKGROUND_COLOR)\","
      if [ -n "$cursor" ]; then
        echo "  cursor_bg = \"$cursor\","
        echo "  cursor_border = \"$cursor\","
      fi
      echo "  ansi = {"
      for i in 01 02 03 04 05 06 07 08; do echo "    \"$(field "COLOR_$i")\","; done
      echo "  },"
      echo "  brights = {"
      for i in 09 10 11 12 13 14 15 16; do echo "    \"$(field "COLOR_$i")\","; done
      echo "  },"
      echo "}"
    } >"$out"
    ;;
  *)
    # kitty / alacritty: gogh already writes their config files.
    :
    ;;
esac
